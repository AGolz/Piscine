#include "ft_do_op.h"

int	add(int a, int b)
{
	return (a + b);
}

int	substract(int a, int b)
{
	return (a - b);
}

int	divide(int a, int b)
{
	return (a / b);
}

int	modulo(int a, int b)
{
	return (a % b);
}

int	multiply(int a, int b)
{
	return (a * b);
}
